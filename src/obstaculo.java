public class obstaculo extends Objeto{
    private boolean activo;

    public obstaculo(boolean activo, int x, int y) {
        super(x, y);
        this.activo = true;
    }
    
    public void mover(){
    
    }

    public boolean isActivo() {
        return activo;
    }

    public void setActivo(boolean activo) {
        this.activo = activo;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
